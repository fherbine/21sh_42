#include "../includes/minishell.h"

void	bi_env(char **envp)
{
	int	i;

	i = 0;
	while (envp[i])
	{
		ft_prints("%s%c", envp[i], (envp[i][0] == 0) ? '\0' : '\n');
		i++;
	}
}
